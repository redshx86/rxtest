void dumpfarr(TCHAR *name, float *buf, size_t len)
{
	FILE *fp;
	size_t i;

	if( (fp = _tfopen(name, _T("wt"))) != NULL )
	{
		fprintf(fp, "[...\n");
		for(i = 0; i < len; i++)
			fprintf(fp, "  %.12f,...\n", buf[i]);
		fprintf(fp, "];\n");

		fclose(fp);
	}
}

